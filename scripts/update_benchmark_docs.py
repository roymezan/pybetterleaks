from __future__ import annotations

import argparse
from collections.abc import Sequence
from pathlib import Path
from typing import Optional

ROOT = Path(__file__).resolve().parents[1]
START_MARKER = "<!-- pybetterleaks-benchmark:start -->"
END_MARKER = "<!-- pybetterleaks-benchmark:end -->"


def main() -> None:
    run()


def run(argv: Optional[Sequence[str]] = None) -> None:
    args = parse_args(argv)
    docs_path = Path(args.docs)
    benchmark_path = Path(args.benchmark_markdown)

    docs_text = docs_path.read_text(encoding="utf-8")
    benchmark_markdown = benchmark_path.read_text(encoding="utf-8")
    source_label = args.source_label or str(benchmark_path)
    updated = replace_managed_section(
        docs_text,
        benchmark_markdown,
        source_label=source_label,
    )

    if args.check:
        if updated != docs_text:
            raise SystemExit(
                f"{docs_path} is not current for {benchmark_path}; "
                "rerun scripts/update_benchmark_docs.py without --check"
            )
        print(f"Benchmark docs are current: {docs_path}")
        return

    docs_path.write_text(updated, encoding="utf-8")
    print(f"Updated {docs_path} from {benchmark_path}")


def parse_args(argv: Optional[Sequence[str]] = None) -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description="Refresh the managed benchmark section in docs/benchmarks.md.",
    )
    parser.add_argument(
        "benchmark_markdown",
        help="Markdown report generated by benchmarks/bench.py --markdown-output.",
    )
    parser.add_argument(
        "--docs",
        default=ROOT / "docs" / "benchmarks.md",
        help="Benchmark documentation file to update.",
    )
    parser.add_argument(
        "--source-label",
        default=None,
        help="Human-readable source label written into an HTML comment.",
    )
    parser.add_argument(
        "--check",
        action="store_true",
        help="Fail if the managed docs section would change.",
    )
    return parser.parse_args(argv)


def replace_managed_section(
    docs_text: str,
    benchmark_markdown: str,
    *,
    source_label: str,
) -> str:
    start = docs_text.find(START_MARKER)
    end = docs_text.find(END_MARKER)
    if start == -1 or end == -1:
        raise ValueError(
            f"docs text must contain {START_MARKER!r} and {END_MARKER!r} markers"
        )
    if end < start:
        raise ValueError(f"{END_MARKER!r} must appear after {START_MARKER!r}")

    section_end = end + len(END_MARKER)
    replacement = format_managed_section(
        benchmark_markdown,
        source_label=source_label,
    )
    return docs_text[:start] + replacement + docs_text[section_end:]


def format_managed_section(benchmark_markdown: str, *, source_label: str) -> str:
    body = demote_headings(benchmark_markdown.strip())
    safe_source = source_label.replace("--", "- -")
    return "\n".join(
        [
            START_MARKER,
            "<!-- Generated by scripts/update_benchmark_docs.py; do not edit by hand. -->",
            f"<!-- Source: {safe_source} -->",
            "",
            body,
            "",
            END_MARKER,
        ]
    )


def demote_headings(markdown: str, *, levels: int = 2) -> str:
    lines = []
    for line in markdown.splitlines():
        stripped = line.lstrip()
        leading_spaces = len(line) - len(stripped)
        if leading_spaces <= 3 and stripped.startswith("#"):
            hashes = len(stripped) - len(stripped.lstrip("#"))
            if hashes > 0 and stripped[hashes : hashes + 1] == " ":
                new_hashes = "#" * min(6, hashes + levels)
                lines.append(line[:leading_spaces] + new_hashes + stripped[hashes:])
                continue
        lines.append(line)
    return "\n".join(lines).rstrip()


if __name__ == "__main__":
    main()
